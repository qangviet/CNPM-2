import {
    readEmployee,
    createEmployee,
    deleteEmployee,
    updateEmployee,
} from "../services/CRUDEmployee";
import { readUser } from "../services/User";

const getEmployee = async (req, res) => {
    try {
        let data = await readEmployee();
        return res.status(200).json({
            EM: data.EM,
            EC: data.EC,
            DT: data.DT,
        });
    } catch (error) {
        console.log(error);
        return res.status(500).json({
            EM: "error from server",
            EC: "-1",
            DT: "",
        });
    }
};

